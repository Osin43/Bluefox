    {
        $request->validate([
            'status'=>'in:pending,complete',
            'quantity'=>'required',
            'type'=>'in:corporate,individual',
            'email'=>'required|email',
            'user_id'=>'required',
            'total'=>'required', 'regex:/^(\d+(\.\d*)?)|(\.\d+)$/',
            'payment'=>'in: online, COD, IPS'
        ]);

        $user = User::create([
            'status' => $request->name,
            'quantity' => $request->quantity,
            'type' => $request->type,
            'email' => $request->email,
            'user_id' => $request->user_id,
            'total'=> $request->total,
            'payment'=> $request->payment,
        ]);

        return  $user;
            
    }

